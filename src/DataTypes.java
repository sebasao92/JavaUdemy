public class DataTypes {
    /**
     * Byte
     * 8 bits (1 byte)
     * Min Value = (-2^7)
     * Max Value = (2^7 - 1)
     */
    byte varByte;

    /**
     * Short
     * 16 bits (4 bytes)
     * Min Value = (-2^15)
     * Max Value = (2^15 - 1)
     */
    short varShort;

    /**
     * Integer
     * 32 bits (8 bytes)
     * Min Value = (-2^31)
     * Max Value = (2^31 - 1)
     */
    int varInt;

    /**
     * Long
     * 64 bits (16 bytes)
     * Min Value = (-2^63)
     * Max Value = (2^63 - 1)
     */
    long varLong;

    /**
     * Float (f)
     * 32 bits (8 bytes)
     */
    float varFloat;

    /**
     * Double (d)
     * 64 bits (16 bytes)
     */
    double varDouble;

    /**
     * boolean
     * 1 bit (true or false)
     */
    boolean varBoolean;

    /**
     * character (char)
     * 16 bits (4 bytes)
     */
    char varChar;

    /**
     * String (No primitive)
     */
    String varString;
}
